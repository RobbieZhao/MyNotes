function normalizeCountryKey(name: string): string {
  return name
    .trim()
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[.'']/g, "")
    .replace(/\s+/g, " ");
}

const ALIAS_TO_ID: Record<string, string> = (() => {
  const map: Record<string, string> = { ...EXTRA_ALIASES };
  for (const [id, name] of Object.entries(TOPO_COUNTRY_NAMES)) {
    map[normalizeCountryKey(name)] = id;
  }
  return map;
})();

export function resolveCountryId(name: string): string | null {
  const key = normalizeCountryKey(name);
  if (!key) return null;

  if (ALIAS_TO_ID[key]) return ALIAS_TO_ID[key];

  const numeric = key.replace(/^0+/, "");
  if (/^\d+$/.test(numeric) && TOPO_COUNTRY_NAMES[numeric.padStart(3, "0")]) {
    return numeric.padStart(3, "0");
  }

  for (const [alias, id] of Object.entries(ALIAS_TO_ID)) {
    if (alias.includes(key) || key.includes(alias)) {
      if (Math.min(alias.length, key.length) >= 4) return id;
    }
  }

  return null;
}

export function getCountryDisplayName(id: string, fallback?: string): string {
  return TOPO_COUNTRY_NAMES[id] ?? fallback ?? id;
}
